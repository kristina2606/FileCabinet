namespace FileCabinetApp
{
    /// <summary>
    /// Represents the type of union operation.
    /// </summary>
    public enum UnionType
    {
        /// <summary>
        /// An operator that combines results by multiple parameters.
        /// </summary>
        And,

        /// <summary>
        /// An operator that produces a result with multiple parameters.
        /// </summary>
        Or,
    }
}